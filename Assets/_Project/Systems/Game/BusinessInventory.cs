public class BusinessInventory
{
    private uint _dollars;
    private uint _beans;
    private uint _rawTeaLeaves;
    private uint _teaBags;
    private uint _milkCartons;

    public uint Dollars { get => _dollars; set => _dollars = value; }
    public uint Beans { get => _beans; set => _beans = value; }
    public uint RawTeaLeaves { get => _rawTeaLeaves; set => _rawTeaLeaves = value; }
    public uint TeaBags { get => _teaBags; set => _teaBags = value; }
    public uint MilkCartons { get => _milkCartons; set => _milkCartons = value; }
}
